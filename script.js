document.addEventListener('DOMContentLoaded', () => {
    // --- I18n Logic ---
    const setLanguage = (lang) => {
      document.documentElement.lang = lang;
      // Update text content
      document.querySelectorAll('[data-i18n-key]').forEach(element => {
        const key = element.getAttribute('data-i18n-key');
        if (translations[lang] && translations[lang][key]) {
          element.innerHTML = translations[lang][key];
        }
      });

      // Update active class on language switcher
      const currentActive = document.querySelector('.lang-selector a.active');
      if (currentActive) {
          currentActive.classList.remove('active');
      }
      const newActiveLink = document.querySelector(`.lang-selector a[data-lang="${lang}"]`);
      if (newActiveLink) {
        newActiveLink.classList.add('active');
      }
    };

    const langSelector = document.querySelector('.lang-selector');
    if (langSelector) {
        langSelector.addEventListener('click', (e) => {
          if (e.target.tagName === 'A' && e.target.dataset.lang) {
            e.preventDefault();
            setLanguage(e.target.dataset.lang);
          }
        });
    }

    // --- Original Script Logic ---
    const menuBtn = document.querySelector('.mobile-menu-btn');
    const navContainer = document.querySelector('.nav-container');

    const toggleMenu = () => {
        if (menuBtn && navContainer) {
            menuBtn.classList.toggle('active');
            navContainer.classList.toggle('active');
            document.body.style.overflow = navContainer.classList.contains('active') ? 'hidden' : '';
        }
    };

    if (menuBtn) {
        menuBtn.addEventListener('click', toggleMenu);
    }

    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                if (navContainer && navContainer.classList.contains('active')) {
                    toggleMenu();
                }

                const headerOffset = 100; // Adjusted for fixed header height
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - headerOffset;

                window.scrollTo({
                    top: offsetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    const letterModal = document.getElementById('letter-modal');
    const openModalBtn = document.getElementById('open-letter-modal');
    const closeModalBtn = document.querySelector('.close-modal');
    const letterForm = document.getElementById('letter-form');

    if (openModalBtn && letterModal) {
        openModalBtn.addEventListener('click', () => {
            letterModal.classList.add('active');
            document.body.style.overflow = 'hidden';
        });
    }

    const closeModal = () => {
        if (letterModal) {
            letterModal.classList.remove('active');
            document.body.style.overflow = '';
        }
    };

    if (closeModalBtn) {
        closeModalBtn.addEventListener('click', closeModal);
    }

    if (letterModal) {
        letterModal.addEventListener('click', (e) => {
            if (e.target === letterModal) closeModal();
        });
    }
    
    // Article Modal Logic
    const articleModal = document.getElementById('article-modal');
    const openArticleBtn = document.getElementById('open-article-modal');
    const closeArticleBtn = document.querySelector('.close-modal-article');

    if (openArticleBtn && articleModal && closeArticleBtn) {
        openArticleBtn.addEventListener('click', () => {
            articleModal.classList.add('active');
            document.body.style.overflow = 'hidden';
        });

        closeArticleBtn.addEventListener('click', () => {
            articleModal.classList.remove('active');
            document.body.style.overflow = 'auto';
        });

        articleModal.addEventListener('click', (e) => {
            if (e.target === articleModal) {
                articleModal.classList.remove('active');
                document.body.style.overflow = 'auto';
            }
        });
    }

    // Set initial language on page load
    // You can enhance this to detect browser language or use localStorage
    setLanguage('ko');
});
